    public function viewCategory($categoryName) {
        $convertName = str_replace("_", " ", $categoryName);
        $category = Category::where("categoryName", $convertName)->get()->first();
        $products = Product::where("categoryId", $category['id'])->get();
        return view('category', compact('category', 'products'));
    }

